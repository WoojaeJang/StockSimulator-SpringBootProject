DROP TABLE CUSTOMER;
DROP TABLE ACCOUNT;

CREATE TABLE CUSTOMER (
    CUSTOMER_ID INT(5) unsigned NOT NULL AUTO_INCREMENT,
    CUSTOMER_NAME VARCHAR(255) NOT NULL,
    CUSTOMER_EMAIL VARCHAR(255) NOT NULL,
    CUSTOMER_PW VARCHAR(255) NOT NULL,
    PRIMARY KEY (CUSTOMER_ID)
);


CREATE TABLE ACCOUNT (
    ACCOUNT_ID INT(5) unsigned NOT NULL AUTO_INCREMENT,
    ACCOUNT_NAME VARCHAR(255) NOT NULL,
    DEPOSIT BIGINT NOT NULL,
    CUSTOMER_ID INT(5) unsigned NOT NULL,
    PRIMARY KEY (ACCOUNT_ID)
);

ALTER TABLE CUSTOMER
ADD CONSTRAINT FK_CUSTOMER_ACCOUNT
FOREIGN KEY (ACCOUNT_ID)
REFERENCES ACCOUNT(ACCOUNT_ID);


